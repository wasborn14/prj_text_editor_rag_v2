// ファイル拡張子からアイコンタイプを判定するヘルパー関数
export function getFileIconType(fileName: string): IconType {
  const extension = fileName.split('.').pop()?.toLowerCase()

  switch (extension) {
    case 'js':
    case 'jsx':
    case 'ts':
    case 'tsx':
    case 'py':
    case 'java':
    case 'cpp':
    case 'c':
    case 'go':
    case 'rs':
    case 'php':
    case 'rb':
      return 'file-code'

    case 'json':
    case 'yaml':
    case 'yml':
    case 'xml':
      return 'file-json'

    case 'png':
    case 'jpg':
    case 'jpeg':
    case 'gif':
    case 'svg':
    case 'webp':
      return 'file-image'

    case 'md':
    case 'txt':
    case 'rtf':
      return 'file-text'

    default:
      return 'file'
  }
}